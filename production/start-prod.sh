    if [ "$DISABLE_ALL_AI_FUNCTIONALITY" = "true" ]; then
        echo "AI functionality is disabled. Starting without 'ollama' and 'webui' service..."
        $dockerCmd compose -f docker-compose.yml --env-file $SCRIPT_DIR/.env up -d
    else
